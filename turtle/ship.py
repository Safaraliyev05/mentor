import turtle

turtle.hideturtle()
windows = turtle.getscreen()
windows.bgcolor("lightskyblue")
windows.setup(800, 500)
t = turtle.Turtle()
t.speed(0)

t.up()
t.goto(-400, -150)
t.down()
t.color("mediumblue")
t.begin_fill()
for i in range(2):
    t.forward(800)
    t.right(90)
    t.forward(100)
    t.right(90)
t.end_fill()

t.up()
t.goto(-200, -80)
t.down()
t.color("saddlebrown")
t.begin_fill()
t.right(45)
t.forward(100)
t.left(45)
t.forward(250)
t.left(45)
t.forward(100)
t.end_fill()

t.up()
t.goto(0, -80)
t.down()
t.color("black")
t.begin_fill()
t.left(45)
for i in range(2):
    t.forward(200)
    t.left(90)
    t.forward(10)
    t.left(90)
t.end_fill()

t.up()
t.goto(0, 120)
t.down()
t.color("white")
t.begin_fill()
t.right(130)
t.forward(250)
t.right(140)
t.forward(190)
t.end_fill()

t.up()
t.forward(14)
t.down()
t.begin_fill()
t.forward(180)
t.right(139)
t.forward(240)
t.end_fill()

t.up()
t.goto(-300, 150)
t.down()
t.color("yellow")
t.begin_fill()
t.circle(40)
t.end_fill()

t.hideturtle()
windows.mainloop()